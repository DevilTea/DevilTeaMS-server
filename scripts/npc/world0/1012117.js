function action(mode, type, selection) {
	if(mode == -1) {
		cm.dispose();
	} else if(mode == 1) {
		status++;
		if(status == 0) {
			cm.sendSimple("即使你的頭再大我都可以剪出漂亮的造型唷~\r\n\r\n#L0##b髮型#l\r\n#L1##b髮色#l");
		} else if(status == 1) {
			if(selection == 0) {
				status = 11;
				var txt = "選項真的很多，比我想像的還多\r\n\r\n";
				if(cm.getPlayer().getGender() == 0) {
					txt += "#b#L0#男生髮型 1#l\r\n#L1#男生髮型 2#l\r\n#L2#男生髮型 3#l\r\n#L3#男生髮型 4#l\r\n";
				} else if(cm.getPlayer().getGender() == 1) {
					txt += "#b#L0#女生髮型 1#l\r\n#L1#女生髮型 2#l\r\n#L2#女生髮型 3#l\r\n#L3#女生髮型 4#l\r\n"
				}
				//txt += "#r#L4#活動髮型#l";
				cm.sendSimple(txt);
			} else if(selection == 1) {
				if(cm.getPlayer().getGender() == 0) {
					hairnew.push(30000);
				} else if(cm.getPlayer().getGender() == 1) {
					hairnew.push(31000);
				}
				var current = cm.getPlayer().getHair() - (cm.getPlayer().getHair() % 10);
				for(var i = 0; i < 8; i++) {
					hairnew.push(current + i);
				}
				cm.sendStyle("選一個喜歡的吧!\r\n\r\n#r(預設髮型為防止斷線所設)", hairnew);
			}
		} else if(status == 2) {
			cm.setHair(hairnew[selection]);
			cm.sendOk("完成囉!");
			cm.dispose();
		} else if(status == 12) {
			if(selection == 4) {
				for(var i = 0; i < hair_event.length; i++) {
					hairnew[hairnew.length] = hair_event[i];
				}
			} else if(cm.getPlayer().getGender() == 0) {
				for(var i = 0; i < hair_m[selection].length; i++) {
					hairnew[hairnew.length] = (hair_m[selection][i] + (cm.getPlayer().getHair() % 10));
				}
			} else if(cm.getPlayer().getGender() == 1) {
				for(var i = 0; i < hair_f[selection].length; i++) {
					hairnew[hairnew.length] = (hair_f[selection][i] + (cm.getPlayer().getHair() % 10));
				}
			}
			cm.sendStyle("選一個喜歡的吧!#r(因為提供的選項極多，勿過快點擊上/下一選項)", hairnew);
		} else if(status == 13) {
			cm.setHair(hairnew[selection]);
			cm.sendOk("完成囉!");
			cm.dispose();
		}
		
	} else {
		cm.sendOk("有需要再來唷!");
		cm.dispose();
		return;
	}
}